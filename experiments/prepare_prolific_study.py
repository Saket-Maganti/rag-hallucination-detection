"""
prepare_prolific_study.py — Item A2 (human validation)
=======================================================

Samples 500 generated answers stratified across the five comparison
conditions (baseline, hcpc_v1, hcpc_v2, selfrag, crag) and packages them
into the artifacts needed to launch a Prolific study:

    annotations_input.csv   — one row per (case_id, condition) for the
                              annotation interface (Google Forms / Qualtrics
                              / a Prolific-hosted form).
    annotation_guide.md     — the rubric annotators see (faithfulness,
                              correctness, helpfulness ratings + free text).
    sample_manifest.csv     — provenance + NLI-faithfulness for every
                              sampled item, used by the analysis script to
                              compute the human-vs-NLI correlation.
    prolific_README.md      — operational notes for the study launch
                              (recruitment, screening, payment).

Sampling design:
    - 100 items per condition (5 conditions × 100 = 500 total).
    - Within each condition, balance across datasets in proportion to the
      condition's representation in results/multidataset/per_query.csv.
    - Two annotators per item -> 1000 annotation tasks total.
    - Estimated time per task: 60-90 seconds.
    - Estimated cost: ~$200-300 on Prolific at £9/hour median rate.

Run:
    python3 experiments/prepare_prolific_study.py \
        --source results/headtohead/per_query.csv \
        --output_dir results/human_validation/study_v1 \
        --n_per_condition 100 \
        --n_annotators 2
"""

from __future__ import annotations

import sys, os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

import argparse
import json
import os
import random
from typing import Dict, List

import pandas as pd

DEFAULT_SOURCE = "results/headtohead/per_query.csv"
DEFAULT_OUTPUT = "results/human_validation/study_v1"


ANNOTATION_GUIDE = """\
# Annotator Guide — Faithfulness Study (Anthropic-style)

You will see a question, a set of source passages, and a candidate answer
generated by an AI system. Your task is to rate the answer along three
dimensions on a 1-5 scale, and to mark a binary faithfulness label.

## Rubric

For each item, provide the following ratings:

1. **Faithful (binary)**
   - YES: every claim in the answer is supported by the source passages.
   - NO:  the answer contains at least one claim that is not supported by
          (or contradicts) the source passages.

2. **Correctness (1-5)**
   - 1 = answer is wrong or completely misleading
   - 3 = answer is mostly correct but with notable errors
   - 5 = answer is fully correct and complete

3. **Helpfulness (1-5)**
   - 1 = answer fails to address the question
   - 3 = answer addresses the question but is incomplete or rambling
   - 5 = answer addresses the question concisely and clearly

4. **Free-text comment (optional)**
   - One sentence noting any specific support gap, error, or commendation.

## Important rules

- **Read the source passages first**, then read the answer. Do NOT use
  external knowledge to fill in gaps; rate strictly against what the
  passages say.
- If the answer states "I cannot find this information in the provided
  context" and the passages indeed do not contain the answer, mark it as
  Faithful=YES (the abstention is honest), Correctness=N/A (use the
  N/A option), Helpfulness based on whether the abstention was
  appropriate.
- Do NOT consider grammar or fluency as part of faithfulness.

## Calibration examples

[3 worked examples are inserted here at study launch — see
calibration_examples.md.]
"""


PROLIFIC_README = """\
# Prolific study setup

## Recruitment
- Filter: English (native or fluent), no AI-tooling experience required.
- Pre-screen with 3 calibration items; reject annotators below 80% agreement
  with the gold rubric.

## Workflow
1. Annotators review `annotation_guide.md` (5 min, paid as bonus).
2. Annotators complete `annotations_input.csv` items in batches of 25.
3. Each item is double-annotated by independent workers (no overlap).

## Payment
- £9/hour target rate (UK Prolific standard).
- 1.5 minutes per item × 25 items per batch = 37.5 minutes per batch ≈ £5.62.
- 1000 items × 2 annotators / 25 per batch = 80 batches → ~£450 total.

## Output ingestion
- Save Prolific exports to `results/human_validation/study_v1/raw_annotations.csv`.
- Run `experiments/analyze_prolific_results.py` to compute Fleiss' kappa,
  per-condition aggregates, and NLI vs human correlations.
"""


def stratified_sample(
    df: pd.DataFrame,
    n_per_condition: int,
    seed: int = 42,
) -> pd.DataFrame:
    """
    Sample N items per condition, balanced across datasets.

    For each condition, sample proportionally across datasets so that the
    condition mix in the human study mirrors the multidataset composition.
    """
    rng = random.Random(seed)
    sampled: List[pd.DataFrame] = []
    if "condition" not in df.columns:
        raise ValueError("source CSV must contain a 'condition' column")

    for cond, sub in df.groupby("condition"):
        sub = sub.dropna(subset=["answer", "question"])
        if len(sub) == 0:
            continue
        if len(sub) <= n_per_condition:
            sampled.append(sub.copy())
            continue

        ds_groups = list(sub.groupby("dataset"))
        ds_counts = {ds: len(g) for ds, g in ds_groups}
        total = sum(ds_counts.values())
        # Target proportion per dataset
        targets = {
            ds: max(1, round(n_per_condition * (ds_counts[ds] / total)))
            for ds in ds_counts
        }
        # Adjust to sum exactly to n_per_condition
        diff = n_per_condition - sum(targets.values())
        keys = list(targets.keys())
        idx = 0
        while diff != 0 and keys:
            key = keys[idx % len(keys)]
            if diff > 0:
                targets[key] += 1; diff -= 1
            else:
                if targets[key] > 1:
                    targets[key] -= 1; diff += 1
            idx += 1
        for ds, g in ds_groups:
            n_take = min(targets.get(ds, 0), len(g))
            if n_take == 0:
                continue
            sampled.append(g.sample(n=n_take, random_state=rng.randrange(1 << 30)))

    return pd.concat(sampled, ignore_index=True)


def build_annotation_csv(sampled: pd.DataFrame) -> pd.DataFrame:
    """Project the sampled rows into annotator-facing fields."""
    out = pd.DataFrame({
        "task_id":          [f"H{i:04d}" for i in range(len(sampled))],
        "question":         sampled["question"].fillna(""),
        "source_passages":  sampled.get(
            "context",
            sampled.get("retrieved_context", "(see manifest)"),
        ),
        "answer":           sampled["answer"].fillna(""),
        # The annotator MUST NOT see condition or NLI score; we hide them
        # by writing them to the manifest CSV instead.
    })
    return out


def build_manifest(sampled: pd.DataFrame) -> pd.DataFrame:
    return pd.DataFrame({
        "task_id":            [f"H{i:04d}" for i in range(len(sampled))],
        "dataset":            sampled.get("dataset", ""),
        "condition":          sampled["condition"],
        "question":           sampled["question"],
        "answer":             sampled["answer"],
        "ground_truth":       sampled.get("ground_truth", ""),
        "nli_faithfulness":   sampled["faithfulness_score"],
        "nli_is_hallucination": sampled["is_hallucination"],
        "source_idx":         sampled.index,
    })


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--source", default=DEFAULT_SOURCE)
    parser.add_argument("--output_dir", default=DEFAULT_OUTPUT)
    parser.add_argument("--n_per_condition", type=int, default=100)
    parser.add_argument("--n_annotators", type=int, default=2)
    args = parser.parse_args()

    if not os.path.exists(args.source):
        raise FileNotFoundError(
            f"Source CSV not found: {args.source}. Run "
            "experiments/run_headtohead_comparison.py first to generate it."
        )

    os.makedirs(args.output_dir, exist_ok=True)
    src = pd.read_csv(args.source)
    src = src[src.get("skipped").isna()] if "skipped" in src.columns else src
    sampled = stratified_sample(src, args.n_per_condition)

    ann_df = build_annotation_csv(sampled)
    man_df = build_manifest(sampled)

    # Replicate per task once per annotator to get the dual-annotation matrix
    expanded = pd.concat([
        ann_df.assign(annotator_id=f"A{i}") for i in range(args.n_annotators)
    ], ignore_index=True)

    expanded.to_csv(os.path.join(args.output_dir, "annotations_input.csv"), index=False)
    man_df.to_csv(os.path.join(args.output_dir, "sample_manifest.csv"), index=False)

    with open(os.path.join(args.output_dir, "annotation_guide.md"), "w") as fh:
        fh.write(ANNOTATION_GUIDE)
    with open(os.path.join(args.output_dir, "prolific_README.md"), "w") as fh:
        fh.write(PROLIFIC_README)

    print(
        f"[Prolific] Wrote {len(ann_df)} items × {args.n_annotators} annotators "
        f"= {len(expanded)} annotation tasks to {args.output_dir}/"
    )


if __name__ == "__main__":
    main()
