"""
experiments/build_topk_table.py — auto-populate Table tab:topk
==============================================================

Reads results/topk_sensitivity/paradox_by_k.csv (produced by
run_topk_sensitivity.py) and rewrites ragpaper/figures/topk_table.tex
with a fully populated LaTeX tabular body. The robustness section
includes this fragment via `\input{figures/topk_table}`.

This closes the loop: after `run_topk_sensitivity.py` finishes,
one command refreshes the paper table without hand-editing.

Outputs:
    ragpaper/figures/topk_table.tex      (LaTeX tabular body)

Usage:
    python3 experiments/build_topk_table.py
"""

from __future__ import annotations

from pathlib import Path

import pandas as pd

ROOT = Path(__file__).resolve().parent.parent
INPUT_CSV = ROOT / "results" / "topk_sensitivity" / "paradox_by_k.csv"
OUT_TEX = ROOT / "ragpaper" / "figures" / "topk_table.tex"


def main() -> None:
    if not INPUT_CSV.exists():
        raise SystemExit(
            f"[topk-table] missing {INPUT_CSV} -- "
            f"run experiments/run_topk_sensitivity.py first"
        )
    df = pd.read_csv(INPUT_CSV)
    if df.empty:
        raise SystemExit("[topk-table] paradox_by_k.csv is empty")

    df = df.sort_values(["dataset", "k"])

    lines = [
        "% Auto-generated by experiments/build_topk_table.py",
        r"\begin{tabular}{lcccccc}",
        r"\toprule",
        r"Dataset & $k$ & faith$_{\textrm{base}}$ & faith$_{\textrm{v1}}$ & "
        r"$\Delta_{\textrm{paradox}}$ & gate~rec. & v$2$~rec. \\",
        r"\midrule",
    ]

    cur_ds = None
    for _, row in df.iterrows():
        if cur_ds is not None and row["dataset"] != cur_ds:
            lines.append(r"\midrule")
        cur_ds = row["dataset"]
        ds_label = {
            "squad":    "SQuAD",
            "pubmedqa": "PubMedQA",
            "hotpotqa": "HotpotQA",
        }.get(row["dataset"], row["dataset"])
        # Bold the largest paradox per dataset
        par = float(row["paradox"])
        par_str = f"$\\mathbf{{{par:+.3f}}}$" if abs(par) >= 0.05 else f"${par:+.3f}$"
        lines.append(
            f"{ds_label} & ${int(row['k'])}$ & "
            f"${row['faith_base']:.3f}$ & "
            f"${row['faith_v1']:.3f}$ & "
            f"{par_str} & "
            f"${row['gate_recovery']:+.3f}$ & "
            f"${row['v2_recovery']:+.3f}$ \\\\"
        )

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")

    OUT_TEX.parent.mkdir(parents=True, exist_ok=True)
    OUT_TEX.write_text("\n".join(lines) + "\n")
    print(f"[topk-table] wrote {OUT_TEX.relative_to(ROOT)} "
          f"({len(df)} rows)")
    print("\nPreview:")
    print(df[["dataset", "k", "paradox", "gate_recovery", "v2_recovery"]]
            .round(3).to_string(index=False))


if __name__ == "__main__":
    main()
